package bj11.id_02;

import java.io.*;

public class Main {
    public static void main(String[] args) throws Exception {
        BufferedReader br = new BufferedReader(new InputStreamReader(System.in));
        String n = br.readLine();
        System.out.println(n); //  sum을 하기 위해서는 n개의 모든수에 접근해야하므로 n번이다.
        System.out.println(1); //  n에 대하여 1차적인 관계를 가진다고 볼 수 있다.

    }    

}
        